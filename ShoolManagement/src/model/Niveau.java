package model;

public class Niveau {
    private int id;
    private String nom;

    public Niveau() {}

    public Niveau(int id, String nom) {
        this.id = id;
        this.nom = nom;
    }

    // Getters & Setters
    public int getId() { return id; }
    public void setId(int id) { this.id = id; }

    public String getNom() { return nom; }
    public void setNom(String nom) { this.nom = nom; }

    @Override
    public String toString() {
        return nom;
    }
}
